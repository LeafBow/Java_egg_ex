public class OperacionesAritmeticas {
    public static void main(String[] args) {
        // Suma
        int number_1 = 10;
        int number_2 = 2;
        System.out.println("La suma de " + number_1 + " mas " + number_2 + " es: " + (number_1 + number_2));
        System.out.println("La resta de " + number_1 + " menos " + number_2 + " es: " + (number_1 - number_2));
        System.out.println("La multiplicacion de " + number_1 + " por " + number_2 + " es: " + (number_1 * number_2));
        System.out.println("La division de " + number_1 + " entre " + number_2 + " es: " + (number_1 / number_2));
        System.out.println("El modulo de " + number_1 + " y " + number_2 + " es: " + (number_1 % number_2));

    }

}
