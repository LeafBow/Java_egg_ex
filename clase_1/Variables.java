

public class Variables {
    public static void main(String[] args)
    {
        boolean value = true;
        char character = 'a';
        String text = "A horse with no name";
        int number = 1;
        long longNumber = 1000000000l;
        float floatNumber = 2.5f;
        System.out.println("This is a boolean variable: " + value);
        System.out.println("This is a char variable: " + character);
        System.out.println("This is a String variable: " + text);
        System.out.println("This is a int variable: " + number);
        System.out.println("This is a long variable: " + longNumber);
        System.out.println("This is a float variable: " + floatNumber);

    }
    
}
